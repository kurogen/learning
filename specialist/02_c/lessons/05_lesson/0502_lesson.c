#include <stdio.h>
#include <stdlib.h>

int main()
{
	int m[100];
	int i,n,s=0;
	//-------------------------------------------------------------------------1
	
	printf("n? ");
	scanf("%d",&n);
	for(i=0;i<n;i++)
	{
		printf("m[%d]? ",i);
		scanf("%d",&m[i]);
	}
	
	//-------------------------------------------------------------------------2
	
	for(i=0;i<n;i++) s+=m[i];
	
	//-------------------------------------------------------------------------3
	
	printf("%d",m[0]);
	for(i=1;i<n;i++) printf("+%d", m[i]);
	printf("=%d\n",s);
	
	return 0;
}